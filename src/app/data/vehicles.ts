export function formatPrice(price: number): string {
  if (price >= 1000000) {
    return `LKR ${(price / 1000000).toFixed(1)}M`;
  }
  return `LKR ${price.toLocaleString()}`;
}

export function formatMileage(km: number): string {
  return `${km.toLocaleString()} km`;
}
